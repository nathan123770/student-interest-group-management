export const signupActivity = id => request.post(`/activities/${id}/signup`)
export const activitySignups = id => request.get(`/activities/${id}/signups`)
export const myActivitySignups = () => request.get('/activities/signups/mine')
export const reviewActivitySignup = (id, data) => request.post(`/activities/signups/${id}/review`, data)
export const categories = () => request.get('/categories')
export const saveCategory = data => data.id ? request.put('/categories', data) : request.post('/categories', data)
