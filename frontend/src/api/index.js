import request from './request'

export const login = data => request.post('/auth/login', data)
export const register = data => request.post('/auth/register', data)
export const me = () => request.get('/auth/me')
export const publicGroups = params => request.get('/groups/public/page', { params })
export const groups = params => request.get('/groups/page', { params })
export const joinedGroups = () => request.get('/groups/joined')
export const groupDetail = id => request.get(`/groups/${id}`)
export const saveGroup = data => data.id ? request.put('/groups', data) : request.post('/groups', data)
export const deleteGroup = id => request.delete(`/groups/${id}`)
export const reviewGroup = (id, auditStatus) => request.post(`/groups/${id}/review`, null, { params: { auditStatus } })
export const uploadImage = file => {
  const formData = new FormData()
  formData.append('file', file)
  return request.post('/upload/image', formData, { headers: { 'Content-Type': 'multipart/form-data' } })
}
